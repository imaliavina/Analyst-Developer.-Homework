x, y = int(x), int(y)

if x>0 and y>0:
    print('1 четверть')
elif x<0 and y>0:
    print('2 четверть')
elif x<0 and y<0:
    print('3 четверть')
elif x>0 and y<0:
    print('4 четверть')
elif x==0:
    print('Точка находится на оси X')
elif y==0:
    print('Точка находится на оси Y')
else:
    print('Ошибка')

