# Напишите программу, которая принимает на вход цифру, обозначающую
# день недели, и проверяет, является ли этот день выходным.
#
# Пример:
#
# - 6 -> да
# - 7 -> да
# - 1 -> нет

num = int(input('Введите цифру от 1 до 7:'))

lst_wrk = list(range(1,6))
lst_hol = list(range(6,8))

if num in lst_wrk:
    print('Будний')
elif num in lst_hol:
    print('Выходной')
else:
    print('НеправильныЙ номер')

# Напишите программу для. проверки
# истинности утверждения ¬(X ⋁ Y ⋁ Z) = ¬X ⋀ ¬Y ⋀ ¬Z для всех значений предикат.

for x in [0, 1]:
    for y in [0, 1]:
        for z in [0, 1]:
            exp = not (x and y and z) == (not x) or (not y) or (not z)
            print(f"x: {x} y: {y} z: {z} --> {exp}")

